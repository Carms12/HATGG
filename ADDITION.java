public class Addition {
    public static void main(String[] args) {
        // Declare two numbers
        int num1 = 5;
        int num2 = 7;
        
        // Perform the addition
        int sum = num1 + num2;
        
        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
